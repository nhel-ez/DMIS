    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $maxValue = Category::max('CategoryID');
        $category = new Category();
        $category->CategoryId = $maxValue+1;
        $category->CategoryTitle = $request->input('CategoryTitle');
        
        $data = $request->all();
        $userCount = Category::where('CategoryTitle', $data['CategoryTitle']);
        if ($userCount->count()) {
            return back()->with('failed', 'Category is already in the database.');  
        } else {
            $category->save();
            return back()->with('success', 'Category created successfully.');
        }
    }

